        <h3>How to set up your Stripe account</h3>
    </div>
    <div class="content">
        <ol>
            <li>Log in to your <a href="https://www.stripe.com/" target="_blank">Stripe account</a></li>
            <li>Click the <strong>Developers</strong> link in the menu</li>
            <li>Toggle the <strong>Live / Sandbox</strong> option depending on your requirements</li>
            <li>Select <strong>API keys</strong> link in the submenu</li>
            <li>Under <strong>Standard Keys</strong> click <strong>Reveal Live Key Token</strong></li>
            <li>Copy the <strong>Secret Key</strong> and paste it in the Configuration tab</li>
        </ol>
